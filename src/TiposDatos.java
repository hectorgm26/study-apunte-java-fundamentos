public class TiposDatos {
    public static void main(String[] args) {
        /*
        Int = entero = son aquellos números que no tienen decimales, pudiendo ser negativos o positivos
        Double o float = número con un punto decimal, pudiendo ser negativo o positivo
        Char = un caracter simple, pudiendo ser una letra, un número o un símbolo. Se escriben SIEMPRE en comillas simples '' (si estuviera un numero solo sin comillas, es un int)
        String = una cadena de caracteres, pudiendo ser letras, números o símbolos
        Boolean = un valor verdadero o falso, true o false
         */

        /* Declaración de variables:
        TipoDato nombreVariable = valor
         */

        int edad = 26;
        double altura = 1.72;
        char letra = 'h';          // LOS DATOS CHAR SIEMPRE DEBEN IR CON COMILLAS SIMPLES
        String nombre = "Héctor";
        boolean esMayorDeEdad = true;

    }
}
