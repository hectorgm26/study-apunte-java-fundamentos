public class Constantes {
    /*
    Las constantes son variables que no cambian su valor durante la ejecución del programa.
    Se declaran con la palabra reservada final
    final + var + nombreVariable = valor
     */
    public static void main(String[] args) {

        final var profesion = "Programador";
        final var cantidadJuiciosGanados = 3;
        final var letraFavorita = 'c';
        final var peso = 80.52;
        final var esColegiado = false;

        // peso = 52,5 Dará error, por ser constante y no poder actualizarse
    }
}
